def _try_publish_dataagent(
    workspace_id: str,
    agent_id: str,
    agent_name: str,
    token: str,
) -> bool:
    """
    Publishes the Data Agent using the official definition-based mechanism.
    Returns True if any method succeeds, False otherwise.  Non-fatal.

    Strategy (in order):
      1. PRIMARY  — getDefinition → add published/ parts → updateDefinition
                    This is the documented way to publish in the Fabric REST API.
                    The presence of ``Files/Config/publish_info.json`` and the
                    ``published/`` folder parts in the definition marks the agent
                    as Published.
      2. FALLBACK — POST to /publish, /activate, /items/{id}/publish, /items/{id}/deploy
      3. FALLBACK — PATCH with {"publishState": "Published"} etc. (undocumented preview fields)
    """
    req_headers = {
        "Authorization": f"Bearer {token}",
        "Content-Type": "application/json",
    }
    base = f"{FABRIC_BASE_URL}/workspaces/{workspace_id}"

    # ── Method 1: getDefinition → inject published parts → updateDefinition ───
    print("   Attempting publish via getDefinition + updateDefinition (official method)...")
    try:
        get_def_resp = requests.post(
            f"{base}/dataAgents/{agent_id}/getDefinition",
            headers=req_headers,
            json={},
            timeout=30,
        )
        if get_def_resp.ok:
            current_parts: List[Dict[str, Any]] = (
                get_def_resp.json()
                .get("definition", {})
                .get("parts", [])
            )
            # Build a lookup of existing draft parts by path so we can mirror
            # them into the published/ namespace.
            draft_parts_by_path = {p["path"]: p for p in current_parts}

            # Start from the full current definition and add / overwrite
            # the published mirror + publish_info.
            updated_parts = list(current_parts)  # preserve all existing parts

            # Mirror every Files/Config/draft/… part into Files/Config/published/…
            for path, part in draft_parts_by_path.items():
                if path.startswith("Files/Config/draft/"):
                    pub_path = path.replace("Files/Config/draft/", "Files/Config/published/", 1)
                    if pub_path not in draft_parts_by_path:
                        updated_parts.append({
                            "path": pub_path,
                            "payload": part["payload"],
                            "payloadType": part.get("payloadType", "InlineBase64"),
                        })

            # Add (or overwrite) publish_info.json — must use $schema + description
            pub_info_path = "Files/Config/publish_info.json"
            pub_info_payload = _b64json({
                "$schema": "1.0.0",
                "description": "Customer360Agent — published at creation",
            })
            updated_parts = [p for p in updated_parts if p["path"] != pub_info_path]
            updated_parts.append({
                "path": pub_info_path,
                "payload": pub_info_payload,
                "payloadType": "InlineBase64",
            })

            upd_resp = requests.post(
                f"{base}/dataAgents/{agent_id}/updateDefinition",
                headers=req_headers,
                json={"definition": {"parts": updated_parts}},
                timeout=60,
            )
            if upd_resp.status_code in (200, 201, 204):
                print(f"   ✓ Agent published via updateDefinition (HTTP {upd_resp.status_code}).")
                return True
            if upd_resp.status_code == 202:
                op_id = (
                    upd_resp.headers.get("x-ms-operation-id")
                    or upd_resp.headers.get("x-ms-operationid")
                )
                if op_id:
                    try:
                        poll_operation(op_id, token, "data agent publish (updateDefinition)")
                    except Exception:
                        pass
                print("   ✓ Agent publish via updateDefinition accepted (202).")
                return True
            print(
                f"   [INFO] updateDefinition publish → HTTP {upd_resp.status_code}: "
                f"{upd_resp.text[:200]}"
            )
        else:
            print(
                f"   [INFO] getDefinition → HTTP {get_def_resp.status_code}: "
                f"{get_def_resp.text[:200]} — falling back to legacy methods."
            )
    except Exception as exc:
        print(f"   [WARN] getDefinition/updateDefinition publish failed (non-fatal): {exc}")

    # ── Method 2: POST-based publish endpoints (legacy fallback) ─────────────
    post_endpoints = [
        f"{base}/dataAgents/{agent_id}/publish",
        f"{base}/dataAgents/{agent_id}/activate",
        f"{base}/items/{agent_id}/publish",
        f"{base}/items/{agent_id}/deploy",
    ]
    for url in post_endpoints:
        for attempt in (1, 2):   # retry once after 10s on 404
            try:
                resp = requests.post(url, headers=req_headers, json={}, timeout=60)
                if resp.status_code in (200, 201, 204):
                    print(f"   ✓ Agent published via POST {url.split('/')[-1]} (HTTP {resp.status_code}).")
                    return True
                if resp.status_code == 202:
                    op_id = (
                        resp.headers.get("x-ms-operation-id")
                        or resp.headers.get("x-ms-operationid")
                    )
                    if op_id:
                        try:
                            poll_operation(op_id, token, "data agent publish")
                        except Exception:
                            pass
                    print(f"   ✓ Agent publish accepted (202) via POST {url.split('/')[-1]}.")
                    return True
                if resp.status_code == 409:
                    print("   Agent already published (409 Conflict) — treating as success.")
                    return True
                if resp.status_code == 404 and attempt == 1:
                    print(f"   404 from {url.split('/')[-1]}, retrying in 10s...")
                    time.sleep(10)
                    continue
                print(f"   [INFO] POST {url.split('/')[-1]} → HTTP {resp.status_code}: {resp.text[:120]}")
                break
            except Exception as exc:
                print(f"   [WARN] POST {url.split('/')[-1]} exception: {exc}")
                break

    # ── Method 3: PATCH-based publish (undocumented preview fields) ───────────
    patch_payloads = [
        {"publishState": "Published"},
        {"state": "Published"},
        {"status": "Published"},
    ]
    patch_url = f"{base}/dataAgents/{agent_id}"
    for payload in patch_payloads:
        field = list(payload.keys())[0]
        try:
            resp = requests.patch(patch_url, headers=req_headers, json=payload, timeout=60)
            if resp.status_code in (200, 201, 204):
                print(f"   ✓ Agent published via PATCH {field}=Published (HTTP {resp.status_code}).")
                return True
            if resp.status_code == 202:
                print(f"   ✓ Agent PATCH publish accepted (202) via {field}.")
                return True
            print(f"   [INFO] PATCH {field}=Published → HTTP {resp.status_code}: {resp.text[:100]}")
        except Exception as exc:
            print(f"   [WARN] PATCH {field}=Published exception: {exc}")

    print(
        f"   [INFO] All publish methods failed — "
        f"the Fabric publish API may not be available on this tenant yet.\n"
        f"   Manual action required: open the Fabric portal, find '{agent_name}',\n"
        f"   and click 'Publish' in the top ribbon.\n"
        f"   Direct link: https://app.fabric.microsoft.com/groups/{workspace_id}"
    )
    return False


