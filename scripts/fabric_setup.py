def infer_columns_from_csv(csv_path: str) -> List[Dict[str, str]]:
    """Infers column definitions from a CSV file header row.

    Each column receives a ``valueType`` of ``"Double"`` when the first data
    row contains a parseable float, or ``"String"`` otherwise.  Only these two
    value types are used; no attempt is made to distinguish integers, booleans,
    or date strings from general strings.

    Args:
        csv_path: Absolute or relative path to the CSV file.

    Returns:
        List of ``{"name": <header>, "valueType": <"String"|"Double">}`` dicts
        in the order the columns appear in the file.
    """
    import csv as _csv

    with open(csv_path, newline="", encoding="utf-8") as fh:
        reader = _csv.DictReader(fh)
        headers: List[str] = list(reader.fieldnames or [])
        first_row: Dict[str, str] = next(iter(reader), {})

    columns: List[Dict[str, str]] = []
    for header in headers:
        value_type = "String"
        raw = first_row.get(header, "")
        try:
            float(raw)
            value_type = "Double"
        except (ValueError, TypeError):
            pass
        columns.append({"name": header, "valueType": value_type})
    return columns


def _build_ontology_parts(
    workspace_id: str,
    lakehouse_id: str,
    table_name: str,
    columns: Optional[List[Dict[str, str]]] = None,
    ontology_name: str = "Customer360Ontology",
) -> List[Dict[str, Any]]:
    """Builds the multi-part ontology definition using Fabric's EntityTypes format.

    The definition always includes:
      .platform                                    — required item metadata (type + displayName)
      definition.json                              — graph model content with nodeTypes + edgeTypes
                                                     (required by GraphSchemaDiscovery+ModelContent
                                                      so the Data Agent can load the graph schema)
      EntityTypes/{id}/definition.json             — entity type definition
      EntityTypes/{id}/DataBindings/{id}.json      — data binding to Lakehouse table

    ROOT CAUSE OF "missing required properties: nodeTypes, edgeTypes":
    When the Data Agent's GraphSchemaDiscovery service reads the ontology it
    deserialises definition.json as ModelContent.  An empty {} causes a hard
    deserialization failure.  Populating definition.json with the actual
    nodeTypes / edgeTypes (derived from the entity type) resolves this.
    The IDs in definition.json match those in EntityTypes/ for consistency.
    """
    import random as _rng
    _rng.seed(42)
    _used: set = set()

    def _gen_id() -> str:
        while True:
            v = _rng.randint(10**12, 10**15)
            if v not in _used:
                _used.add(v)
                return str(v)

    def _to_b64(obj: Any) -> str:
        return base64.b64encode(json.dumps(obj).encode("utf-8")).decode("utf-8")

    # Hard-coded Customer360 schema if columns were not inferred.
    if not columns:
        columns = [
            {"name": "CustomerId",       "valueType": "String"},
            {"name": "FullName",         "valueType": "String"},
            {"name": "State",            "valueType": "String"},
            {"name": "City",             "valueType": "String"},
            {"name": "Segment",          "valueType": "String"},
            {"name": "LifetimeValue",    "valueType": "Double"},
            {"name": "MonthlyRevenue",   "valueType": "Double"},
            {"name": "ChurnRiskScore",   "valueType": "Double"},
            {"name": "LastPurchaseDate", "valueType": "DateTime"},
        ]

    entity_name    = "Customer"
    key_column     = "CustomerId"
    display_column = "FullName"

    # ── Generate stable IDs ─────────────────────────────────────────────────
    entity_type_id = _gen_id()
    property_ids: Dict[str, str] = {}

    # Build properties from columns (shared by EntityType and nodeTypes schema).
    properties = []
    for col in columns:
        prop_id = _gen_id()
        property_ids[col["name"]] = prop_id
        properties.append({
            "id": prop_id,
            "name": col["name"],
            "redefines": None,
            "baseTypeNamespaceType": None,
            "valueType": col["valueType"],
        })

    key_prop_id     = property_ids.get(key_column, properties[0]["id"])
    display_prop_id = property_ids.get(display_column, key_prop_id)

    # ── definition.json — graph model content ───────────────────────────────
    # GraphSchemaDiscovery+ModelContent REQUIRES nodeTypes and edgeTypes.
    # Use the same entity_type_id / property IDs as the EntityTypes folder
    # so both representations are consistent.
    node_type_schema = {
        "id": entity_type_id,
        "name": entity_name,
        "namespace": "usertypes",
        "namespaceType": "Custom",
        "properties": [
            {"id": property_ids[col["name"]], "name": col["name"], "valueType": col["valueType"]}
            for col in columns
            if col["name"] in property_ids
        ],
    }
    definition_content = {
        "nodeTypes": [node_type_schema],
        "edgeTypes": [],
    }

    # ── Assemble parts ──────────────────────────────────────────────────────
    # .platform is required per the Fabric Ontology REST API spec.
    parts: List[Dict[str, Any]] = [
        {
            "path": ".platform",
            "payload": _to_b64({"metadata": {"type": "Ontology", "displayName": ontology_name}}),
            "payloadType": "InlineBase64",
        },
        {
            "path": "definition.json",
            "payload": _to_b64(definition_content),
            "payloadType": "InlineBase64",
        },
    ]

    # ── EntityTypes/{id}/definition.json ────────────────────────────────────
    entity_def = {
        "id": entity_type_id,
        "namespace": "usertypes",
        "baseEntityTypeId": None,
        "name": entity_name,
        "entityIdParts": [key_prop_id],
        "displayNamePropertyId": display_prop_id,
        "namespaceType": "Custom",
        "visibility": "Visible",
        "properties": properties,
        "timeseriesProperties": [],
    }
    parts.append({
        "path": f"EntityTypes/{entity_type_id}/definition.json",
        "payload": _to_b64(entity_def),
        "payloadType": "InlineBase64",
    })

    # ── EntityTypes/{id}/DataBindings/{id}.json ─────────────────────────────
    binding_id = str(uuid.uuid4())
    data_binding = {
        "id": binding_id,
        "dataBindingConfiguration": {
            "dataBindingType": "NonTimeSeries",
            "propertyBindings": [
                {
                    "sourceColumnName": col["name"],
                    "targetPropertyId": property_ids[col["name"]],
                }
                for col in columns
            ],
            "sourceTableProperties": {
                "sourceType": "LakehouseTable",
                "workspaceId": workspace_id,
                "itemId": lakehouse_id,
                "sourceTableName": table_name,
                "sourceSchema": "dbo",
            },
        },
    }
    parts.append({
        "path": f"EntityTypes/{entity_type_id}/DataBindings/{binding_id}.json",
        "payload": _to_b64(data_binding),
        "payloadType": "InlineBase64",
    })

    return parts


def create_ontology(
    workspace_id: str,
    ontology_name: str,
    token: str,
    semantic_model_id: str = "",
    lakehouse_id: str = "",
    table_name: str = "",
    columns: Optional[List[Dict[str, str]]] = None,
) -> str:
    """Creates a Fabric IQ Ontology with a Customer entity bound to the Lakehouse table.

    Uses the multi-part definition format (EntityTypes + DataBindings) that
    correctly populates entities and data bindings in the Fabric Ontology UI.
    This format matches the working pattern from the Trucking Ontology Workshop
    reference notebooks.

    Handles both synchronous (HTTP 201) and asynchronous (HTTP 202) provisioning.

    Raises RuntimeError when creation fails.
    """
    print("🧠 Step: Creating Fabric IQ ontology")

    # Always delete-then-recreate to ensure the correct multi-part entity
    # definition (EntityTypes + DataBindings) is applied.  An ontology that
    # was previously created with the broken {"semanticModelId": "..."} payload
    # will appear in the list with no entity types — returning that stale ID
    # would leave the Data Agent without semantic context.
    try:
        list_resp = fabric_request(
            "GET",
            f"/workspaces/{workspace_id}/items?type=Ontology",
            token,
        )
        for item in list_resp.json().get("value", []):
            if item.get("displayName") == ontology_name:
                old_id = item["id"]
                print(
                    f"   Found existing ontology '{ontology_name}' (ID: {old_id}) — "
                    "deleting to rebuild with correct entity definition..."
                )
                try:
                    del_resp = requests.delete(
                        f"{FABRIC_BASE_URL}/workspaces/{workspace_id}/ontologies/{old_id}",
                        headers={"Authorization": f"Bearer {token}"},
                        timeout=30,
                    )
                    if del_resp.ok or del_resp.status_code == 404:
                        print("   ✓ Existing ontology deleted. Waiting 30s for Fabric cleanup...")
                        time.sleep(30)
                    else:
                        # Fallback: try the items endpoint
                        del_resp2 = requests.delete(
                            f"{FABRIC_BASE_URL}/workspaces/{workspace_id}/items/{old_id}",
                            headers={"Authorization": f"Bearer {token}"},
                            timeout=30,
                        )
                        if del_resp2.ok or del_resp2.status_code == 404:
                            print("   ✓ Existing ontology deleted (items endpoint). Waiting 30s...")
                            time.sleep(30)
                        else:
                            print(
                                f"   [WARN] Could not delete existing ontology "
                                f"(HTTP {del_resp2.status_code}) — will attempt creation anyway."
                            )
                except Exception as del_exc:
                    print(f"   [WARN] Delete attempt failed (non-fatal): {del_exc}")
                break  # Only one ontology with this name can exist
    except Exception as exc:
        print(f"   [WARN] Could not list ontologies (non-fatal): {exc}. Proceeding to attempt creation.")

    url = f"{FABRIC_BASE_URL}/workspaces/{workspace_id}/ontologies"

    # Always build the full multi-part definition with:
    #   .platform              — required item metadata (type = Ontology, displayName)
    #   definition.json        — graph ModelContent with nodeTypes + edgeTypes
    #                            (fixes: GraphSchemaDiscovery+ModelContent missing
    #                             required properties 'nodeTypes', 'edgeTypes')
    #   EntityTypes/{id}/…     — entity type + data binding to Lakehouse table
    parts = _build_ontology_parts(
        workspace_id=workspace_id,
        lakehouse_id=lakehouse_id,
        table_name=table_name or "Customer360",
        columns=columns,
        ontology_name=ontology_name,
    )

    print(f"   Ontology definition: {len(parts)} parts (.platform + graph schema + entity types + data bindings)")

    payload: Dict[str, Any] = {
        "displayName": ontology_name,
        "description": "Customer360 ontology — Customer entity bound to Lakehouse Delta table.",
        "definition": {"parts": parts},
    }

    resp = requests.post(
        url,
        headers={
            "Authorization": f"Bearer {token}",
            "Content-Type": "application/json",
        },
        json=payload,
        timeout=60,
    )

    # SUCCESS CASE (sync)
    if resp.status_code in (200, 201):
        ontology_id = resp.json()["id"]
        print(f"✓ Ontology created: {ontology_id}")
        # Wait for Fabric to index the entity types and build the graph model.
        # Without this wait the Data Agent's GraphSchemaDiscovery may read
        # the ontology before indexing completes and get an empty schema.
        print(f"   Waiting {ONTOLOGY_PROPAGATION_WAIT_SECONDS}s for Fabric to index entity types...")
        time.sleep(ONTOLOGY_PROPAGATION_WAIT_SECONDS)
        return ontology_id

    # ASYNC CASE (most common in Fabric preview)
    if resp.status_code == 202:
        operation_id = (
            resp.headers.get("x-ms-operation-id")
            or resp.headers.get("x-ms-operationid")
            or resp.json().get("operationId")
        )

        print("Ontology provisioning started...")
        if operation_id:
            poll_operation(operation_id, token, "ontology creation")

        # Fabric creates 3 backend resources (Ontology, Graph Model, Ontology
        # Lakehouse) — wait for them to propagate before fetching the ID.
        print("Waiting for ontology backend provisioning...")
        time.sleep(ONTOLOGY_PROPAGATION_WAIT_SECONDS)

        # Fetch ontology ID after provisioning completes.
        list_resp = fabric_request(
            "GET",
            f"/workspaces/{workspace_id}/items?type=Ontology",
            token,
        )
        for item in list_resp.json().get("value", []):
            if item.get("displayName") == ontology_name:
                ontology_id = item["id"]
                print(f"✓ Ontology ready: {ontology_id}")
                return ontology_id

        raise RuntimeError(
            f"Ontology '{ontology_name}' not found after async provisioning completed."
        )

    raise RuntimeError(
        f"Ontology creation failed: {resp.status_code} {resp.text}"
    )


